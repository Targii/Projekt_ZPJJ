package com.mycompany.mavenproject1.entity;

import java.io.Serializable;
import java.util.List;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;

/**
 *
 * @author Adam
 */
@Entity
@Table(schema = "web", name = "tags")
      @NamedQueries({
    @NamedQuery(name = "Tags.byTag", query = "SELECT u FROM TagEntity u WHERE u.tag = :tag"),
    @NamedQuery(name = "Tags.allTags", query = "SELECT u FROM TagEntity u")
          
})
public class TagEntity implements Serializable{
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Long id;
    
    @Column(name = "tag")
    private String tag;
    
    
    @Column(name = "notes")
    private String notes;

    public static long getSerialVersionUID() {
        return serialVersionUID;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }


    
    
}
